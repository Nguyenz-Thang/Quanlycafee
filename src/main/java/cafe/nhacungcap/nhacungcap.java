    public void load_nhacungcap() {
        try {
            Connection con;
            con = ConnectDB.KetnoiDB();
            Statement st = con.createStatement();
            String sql = "Select * From nhacungcap";
            ResultSet rs = st.executeQuery(sql);
            //   tbLoaiSach.removeAll();
            String[] arr = {"Mã công ty", "Tên công ty", "Địa chỉ", "Số điện thoại", "Email"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("macongty"));
                v.add(rs.getString("tencongty"));
                v.add(rs.getString("diachi"));
                v.add(rs.getString("sdt"));
                v.add(rs.getString("email"));
                model.addRow(v);
            }
            ncc.setModel(model);
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
