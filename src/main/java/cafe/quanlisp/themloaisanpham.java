    private void buttonluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonluuActionPerformed
        //lấy thông tin các trường nhập liệu
        String maloai = nhapmaloaitxt.getText().trim();
        String tenloai = tenmltxt.getText().trim();
     
         if (maloai.isEmpty() || tenloai.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin mã loại và tên loại sản phẩm!");
        return;
    }
       
        try {
            
            Connection con = ConnectDB.KetnoiDB();
            String sql = "INSERT into loaisanpham Values('" + maloai + "',N'" + tenloai +"')";

            Statement st = con.createStatement();
            st.executeUpdate(sql);
            con.close();
            JOptionPane.showMessageDialog(this, "Thêm mới thành công !");
         //cập nhạt dữ liệu vào form loaisanpham
            if (parentForm != null) {
                parentForm.load_themloaisanpham();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

        //dong form them san pham sau khi luu thanh cong
        this.dispose();
        formloaisanpham sp = new formloaisanpham();
        sp.setVisible(true);

    }//GEN-LAST:event_buttonluuActionPerformed


